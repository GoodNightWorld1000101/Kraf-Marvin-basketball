void CDC_On_Receive(uint8_t* buffer, uint32_t* length) {
  if (*length == sizeof(Command)) {
    memcpy(&command, buffer, sizeof(Command));

    if (command.delimiter == 0xAAAA) {
      isCommandReceived = 1;
    }
  }
}

int32_t clamp(int32_t val, int32_t min, int32_t max) {
	  if (val < min) return min;
	  if (val > max) return max;
	  return val;
}



int32_t PI_function(Motor* motor, int16_t position) {
    motor->positionChange = position - motor->position;
    int16_t error = motor->speed - motor->positionChange;

    motor->integral += error * motor->gainI;

    motor->integral = clamp(motor->integral, -65535, 65535);

    motor->position = position;
    return motor->gainP * error + motor->integral;
}

void nSleep_pin_toggle(){

    HAL_GPIO_WritePin(nSLEEP_GPIO_Port, nSLEEP_Pin, 0);
    for (int i = 0; i < 400; i++) {
        __NOP();
    }
    HAL_GPIO_WritePin(nSLEEP_GPIO_Port, nSLEEP_Pin, 1);
}

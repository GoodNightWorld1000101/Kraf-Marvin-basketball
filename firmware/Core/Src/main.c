void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim7){
	failsafeCounter++;

	int16_t timer1 = (int16_t)TIM4->CNT;
	int16_t timer2 = (int16_t)TIM2->CNT;
	int16_t timer3 = (int16_t)TIM3->CNT;

	int32_t M1_PWM = PI_function(&motor1, -timer1);
	int32_t M2_PWM = PI_function(&motor2, -timer2);
	int32_t M3_PWM = PI_function(&motor3, timer3);

	//Mootor1
	if (M1_PWM < 0) {
	    HAL_GPIO_WritePin(MOTOR1_DIRECTION_GPIO_Port, MOTOR1_DIRECTION_Pin, GPIO_PIN_SET);
	    M1_PWM *= -1;
	} else {
	    HAL_GPIO_WritePin(MOTOR1_DIRECTION_GPIO_Port, MOTOR1_DIRECTION_Pin, GPIO_PIN_RESET);
	}
	TIM1->CCR3 = (uint32_t)clamp(M1_PWM, 0, 65535);


	//Mootor2

	if (M2_PWM < 0) {
	    HAL_GPIO_WritePin(MOTOR2_DIRECTION_GPIO_Port, MOTOR2_DIRECTION_Pin, GPIO_PIN_SET);
	    M2_PWM *= -1;
	} else {
	    HAL_GPIO_WritePin(MOTOR2_DIRECTION_GPIO_Port, MOTOR2_DIRECTION_Pin, GPIO_PIN_RESET);
	}
	TIM1->CCR2 = (uint32_t)clamp(M2_PWM, 0, 65535);

	//Mootor3
	if (M3_PWM < 0) {
	    HAL_GPIO_WritePin(MOTOR3_DIRECTION_GPIO_Port, MOTOR3_DIRECTION_Pin, GPIO_PIN_SET);
	    M3_PWM *= -1;
	} else {
	    HAL_GPIO_WritePin(MOTOR3_DIRECTION_GPIO_Port, MOTOR3_DIRECTION_Pin, GPIO_PIN_RESET);
	}

	TIM1->CCR1 = (uint32_t)clamp(M3_PWM, 0, 65535);
}
